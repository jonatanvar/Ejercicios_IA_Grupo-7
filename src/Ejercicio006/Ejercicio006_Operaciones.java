package Ejercicio006;

public class Ejercicio006_Operaciones {
    public static double sumar(double a, double b) {
        return a + b;
    }

    public static double restar(double a, double b) {
        return a - b;
    }

    public static double dividir(double a, double b) {
        return a / b;
    }
}